package com.forlooptest.mypackage;


public class forlooptest {

	public static void main(String[] args) {
		
		//increment condition
		for(int x=0;x<=10;x++) {			
			System.out.println(x);
		}
		
		//decrement condition
		for(int x=10;x>=0;x--) {			
			System.out.println(x);
		}
		
		
	}

}
